function changeImg(name) {
    if(name==="apple"){
    $("img").attr('src','./img/apple.jpg')
  }else if(name==="banana"){
    $("img").attr('src','./img/banana.jpg')
  }else if(name==="grape"){
    $("img").attr('src','./img/grape.jpg')
  }else if(name==="peach"){
    $("img").attr('src','./img/peach.jpg')
  }}
